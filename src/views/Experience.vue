<template>
  <Navbar />
  <main>
    <div class="experience">
      <div class="experience__window">
        <ExperienceWindowTabs :type="type" />
        <div class="experience__window__table">
          <div
            ref="shadowContainer"
            class="shadow-container"
            @scroll.capture="setShadows"
          >
            <div class="experience__window__table__wrapper">
              <ExperienceListBlock
                v-for="(semesterData, semester) in classifiedExperiences[type]"
                :key="semester"
                :semester="semester"
              >
                <ExperienceListItem
                  v-for="experience in semesterData"
                  :key="experience.id"
                  :experience="experience"
                  @delete="handleDelete"
                  @edit="handleEditExperience"
                />
              </ExperienceListBlock>
              <button
                class="experience__window__table__add"
                @click="handleAddExperience"
              >
                <svg
                  width="22"
                  height="22"
                  viewBox="0 0 22 22"
                  xmlns="http://www.w3.org/2000/svg"
                >
                  <path
                    d="M8.71431 22.3811H13.2913V13.4791H21.7733V9.0701H13.2913V0.126143H8.71431V9.0701H0.232251V13.4791H8.71431V22.3811Z"
                    fill="white"
                  />
                </svg>
              </button>
            </div>
          </div>
        </div>
      </div>
    </div>
    <FormModal
      v-if="showFormModal"
      :form-type="type"
      :edit-data="experienceToEdit"
      @close="showFormModal = false"
      @submit="handleSubmit"
    />
  </main>
</template>

<script>
import { ref, onMounted, onUpdated } from 'vue'
import ExperienceWindowTabs from '@/components/Experience/ExperienceWindowTabs.vue'
import ExperienceListItem from '@/components/Experience/ExperienceListItem.vue'
import ExperienceListBlock from '@/components/Experience/ExperienceListBlock.vue'
import FormModal from '@/components/FormModal.vue'
import Navbar from '@/components/Navbar'
import useScrollShadow from '@/composables/useScrollShadow'
import getExperiences from '@/composables/experiences/getExperiences'
import getTags from '@/composables/tags/getTags'

export default {
  name: 'Experience',
  components: {
    ExperienceWindowTabs,
    ExperienceListItem,
    ExperienceListBlock,
    FormModal,
    Navbar
  },
  props: {
    // 目前顯示的TAB種類
    type: {
      type: String,
      default: 'course'
    }
  },
  setup (props) {
    const { experiences, classifiedExperiences, reloadExperiences } = getExperiences()
    getTags()

    // ===新增活動表單===
    const showFormModal = ref(false)
    const handleAddExperience = () => {
      showFormModal.value = true
      experienceToEdit.value = null
    }

    // ===設定滾動容器陰影===
    const { setShadows, initShadows } = useScrollShadow()
    const shadowContainer = ref(null)
    onMounted(() => {
      initShadows(shadowContainer.value)
    })
    onUpdated(() => {
      initShadows(shadowContainer.value)
    })

    // ===處理表單送出===
    const handleSubmit = () => {
      showFormModal.value = false
      reloadExperiences()
    }

    // ===處理經驗刪除===
    const handleDelete = () => {
      reloadExperiences()
    }

    // ===點擊編輯的按鈕時，抓出此筆經歷，傳入表單中===
    const experienceToEdit = ref(null)
    const handleEditExperience = (experienceId) => {
      showFormModal.value = true;
      [experienceToEdit.value] = experiences.value[props.type].filter(exp => {
        return exp.id === experienceId
      })
    }

    return { classifiedExperiences, showFormModal, handleAddExperience, setShadows, shadowContainer, handleSubmit, handleDelete, handleEditExperience, experienceToEdit }
  }
}
</script>

